package SetterInjection;

public class Parent {
	String pname;
	String pdept;
	int pid;
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPdept() {
		return pdept;
	}
	public void setPdept(String pdept) {
		this.pdept = pdept;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	
	
}
